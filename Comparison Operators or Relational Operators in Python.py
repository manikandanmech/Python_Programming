#Comparison Operators or Relational Operators in Python

""" Relational operators compares the values of two operands and returns TRUE or FALSE based on whether the condition is met.
-----------------------------------------------------
Operator        	uses
-----------------------------------------------------
==    	      Equal operator
!=      	Not Equal operator
<	        Less than operator
>	        Greater than operator
<=	        Less than or equal to operator
>=	        Greater than or equal to operator
----------------------------------------------------
 """
a = 10
b = 20
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)